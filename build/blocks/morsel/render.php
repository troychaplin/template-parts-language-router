<?php
/**
 * Render a single Morsel (Tidbit post) on the front end.
 *
 * @param array    $attributes The block attributes.
 * @param string   $content    Not used (dynamic block).
 * @param WP_Block $block      The block instance.
 *
 * @package Tidbits
 */

if ( ! defined( 'ABSPATH' ) ) {
	exit;
}

$tidbits_post_id = $attributes['postId'] ?? 0;

if ( ! $tidbits_post_id ) {
	return;
}

$tidbits_post = get_post( $tidbits_post_id );

if ( ! $tidbits_post || 'tidbit' !== $tidbits_post->post_type || 'publish' !== $tidbits_post->post_status ) {
	return;
}

$tidbits_display_mode = $block->context['tidbits/displayMode'] ?? 'accordion';
$tidbits_title        = esc_html( get_the_title( $tidbits_post ) );
// phpcs:ignore WordPress.NamingConventions.PrefixAllGlobals.NonPrefixedHooknameFound -- Core filter required to render block content.
$tidbits_content      = apply_filters( 'the_content', $tidbits_post->post_content );
$tidbits_morsel_id    = 'morsel-' . $tidbits_post_id;
$tidbits_term_id      = 'morsel-term-' . $tidbits_post_id;

if ( 'accordion' === $tidbits_display_mode ) :
	$tidbits_context = wp_interactivity_data_wp_context( array( 'isOpen' => false ) );
	?>
	<div class="tidbits-morsel tidbits-morsel--accordion" <?php echo $tidbits_context; // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Generated by wp_interactivity_data_wp_context. ?>>
		<dt class="tidbits-morsel__term">
			<button
				type="button"
				class="tidbits-morsel__trigger"
				id="<?php echo esc_attr( $tidbits_term_id ); ?>"
				data-wp-on--click="actions.toggle"
				data-wp-bind--aria-expanded="context.isOpen"
				aria-controls="<?php echo esc_attr( $tidbits_morsel_id ); ?>"
				aria-expanded="false"
			>
				<?php // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Already escaped with esc_html above. ?>
				<span class="tidbits-morsel__title"><?php echo $tidbits_title; ?></span>
				<svg
					class="tidbits-morsel__icon"
					data-wp-class--is-open="context.isOpen"
					xmlns="http://www.w3.org/2000/svg"
					width="20"
					height="20"
					viewBox="0 0 24 24"
					fill="none"
					stroke="currentColor"
					stroke-width="2"
					stroke-linecap="round"
					stroke-linejoin="round"
					aria-hidden="true"
				>
					<polyline points="9 18 15 12 9 6"></polyline>
				</svg>
			</button>
		</dt>
		<dd
			class="tidbits-morsel__content"
			id="<?php echo esc_attr( $tidbits_morsel_id ); ?>"
			role="region"
			aria-labelledby="<?php echo esc_attr( $tidbits_term_id ); ?>"
			data-wp-class--is-open="context.isOpen"
			data-wp-bind--aria-hidden="!context.isOpen"
		>
			<div class="tidbits-morsel__inner">
				<?php echo wp_kses_post( $tidbits_content ); ?>
			</div>
		</dd>
	</div>
	<?php
else :
	$tidbits_mode_class = 'columns' === $tidbits_display_mode ? 'tidbits-morsel--columns' : 'tidbits-morsel--stacked';
	?>
	<div class="tidbits-morsel <?php echo esc_attr( $tidbits_mode_class ); ?>">
		<?php // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Already escaped with esc_html above. ?>
		<dt class="tidbits-morsel__term"><?php echo $tidbits_title; ?></dt>
		<dd class="tidbits-morsel__content">
			<?php echo wp_kses_post( $tidbits_content ); ?>
		</dd>
	</div>
	<?php
endif;
